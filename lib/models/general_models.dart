class LottieAnimationBackground {
  final String animationPath;
  final int width;
  final double x;
  final double y;
  final double blur;
  final bool active;
  final double opacity;

  const LottieAnimationBackground({
    required this.animationPath,
    required this.width,
    required this.x,
    required this.y,
    required this.blur,
    required this.active,
    required this.opacity,
  });
}
